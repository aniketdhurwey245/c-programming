#include<iostream>
using namespace std;
int main(){

//integer data type 
int apples = 10;
cout<<apples<<endl;
cout<<"size of int:" <<sizeof(apples)<<endl;

//character data type
char sign = '&';
cout<<sign<<endl;
cout<<"size of char:"<<sizeof(sign)<<endl;

//boolean data type
bool flag = true;
cout<<flag<<endl;
cout<<"size of flag:" <<sizeof(flag)<<endl;

//float data type
float score = 3.67;
cout<<score<<endl;
cout<<"size of float :"<<sizeof(score)<<endl;




    return 0;
}